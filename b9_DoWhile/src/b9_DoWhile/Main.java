package b9_DoWhile;

public class Main {

	public static void main(String[] args) {
		/* 
		  
		  do {
  			// Yapılacak işlemler
				
				} while (koşul);

		  	do-while döngüsü, belirli bir kod bloğunu en az bir kez çalıştıran ve daha sonra bir koşulu kontrol eden bir döngü türüdür.
		  
		 */
		
		int i = 1;
		
		do {
			System.out.println(i);
			i++;
		}while(i < 10);
			

	}

}
