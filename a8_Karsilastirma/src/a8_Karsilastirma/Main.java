package a8_Karsilastirma;

public class Main {

	public static void main(String[] args) {
		/*
			== ---> Eşit mi?
			!= ---> Eşit değil mi?
			< ---> Küçük mü?
			> ---> Büyük mü? 
			<= ---> Küçük veya eşit mi?
			>= ---> Büyük veya eşit mi?

		*/
		
		System.out.println(3 == 3); // içerdeki verilen doğruysa true yanlışsa false döndürür
		System.out.println(3 == 4);
		System.out.println(3 != 3); 
		System.out.println(3 != 4);
		System.out.println(3 < 3); 
		System.out.println(3 < 4);
		System.out.println(3 > 3);
		System.out.println(3 > 4);
		System.out.println(3 <= 3); 
		System.out.println(3 <= 4);
		System.out.println(3 >= 3);
		System.out.println(3 >= 4);
	}

}
